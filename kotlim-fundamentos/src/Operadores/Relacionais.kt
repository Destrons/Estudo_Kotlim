package Operadores

fun main() {
    val a = 10
    val b = 5
    val c = 5

    // Maior que
    println(a > b)

    // Menor que
    println(a < b)

    // Maior ou Igual
    println(b >= c)

    // Menor ou igual
    println(a <= c)

    // Igual
    println(b == c) // 2 igual "=" e usado para igualidade estrtural.
    println(b === c) // 3 igual "=" e usado para igualidade referencial.

}