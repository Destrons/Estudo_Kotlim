package variaveis

fun main() {
    // Tipos n√∫mericos
    /*
    * Byte      - 8
    * Short     - 16
    * Int       - 32
    * Long      - 64
    * Float     - 32
    * Double    - 64
     */

    val byte: Byte = 8
    val short: Short = 360
    val int: Int = 1000
    val long: Long = 10000
    val float: Float = 150.10F
    val double: Double = 300.10

    println(byte)
    println(short)
    println(int)
    println(long)
    println(float)
    println(double)

    // Tipo Booleano
    /*
    * True False
     */

    val boolean: Boolean = false

    println(boolean)

    // Tipos textuais
    /*
    * Char quarda um unico caracter e usa aspas simples
    * String quarda uma cadeia de caracteres e usa aspas duplas
     */

    val char: Char = 'c'
    val string: String = "Thomas"

    println(char)
    println(string)
}